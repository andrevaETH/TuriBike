package ch.ethz.ikg.gis.cyclezurich;

/**
 * Class for info of rental stations
 */
public class MyrentalInfo {

    public String Adresse;
    public String Name;
    public String Telefon;
    public String Url;

    public MyrentalInfo (String Adresse, String Name, String Telefon, String Url) {
        this.Adresse = Adresse;
        this.Name = Name;
        this.Telefon = Telefon;
        this.Url = Url;
    }
}
